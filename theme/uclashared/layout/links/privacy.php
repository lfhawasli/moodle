<h1 class="headingblock">CCLE privacy policy</h1>
<p>CCLE is actively concerned about protecting the privacy of the students, faculty, staff and others who use the UCLA Common Collaboration and Learning
    Environment site. UCLA complies with the Federal Family Educational Rights and Privacy Act (FERPA), the California Information Practices Act, and
    University of California Policies Applying to the Disclosure of Information from Student Records.</p>

<h2>Course privacy</h2>
<p>Courses managed through the CCLE Moodle application make use of technologies that make students' names and email addresses visible within the course
    web site. This information is only visible to other authenticated students, faculty, and participants in the same class.</p>

<h2>Security</h2>
<p>CCLE provides secure, password-protected course web sites. Although no computer system is 100% secure, UCLA has deployed extensive security measures to
    protect against the loss, misuse, or alteration of the information under its control.</p>

<h2>Student privacy</h2>
<p>UCLA, in accordance with the Federal and State Laws and the University Policies, has designated certain categories of personally identifiable
    information as &quot;public information&quot; which UCLA may release and publish without the student's prior consent. See <a
        href="http://www.adminpolicies.ucla.edu/pdf/220.pdf">http://www.adminpolicies.ucla.edu/pdf/220.pdf</a>. Students who do not wish this &quot;public
    information&quot; released may provide written notification to the Registrar so indicating (a &quot;FERPA Hold&quot;). Within CCLE the only personally identifiable
    information disclosed is an individual's name and university e-mail address (as designated in URSA). Please note that all students, including those with a
    FERPA Hold, still will have their names and email addresses viewable within the CCLE Moodle environment. This is fully consistent with FERPA and the
    related regulations.</p>

<h2>Personal disclosures</h2>
<p>Information in electronic form is easily reproduced and easily distributed. For this reason, you should keep in mind that information you share in
    e-mail correspondence, discussion forums, or web pages could be distributed in ways you did not intend. For instance, if you disclose your home phone
    number in an online discussion, another student could copy that phone number and distribute it to others outside the class. Likewise, if you use a
    discussion forum or e-mail to relate a personal experience to your classmates, expecting it to remain private, it could be copied and distributed to
    others outside that group. Therefore, you should exercise caution in sharing information you would not want made public.</p>

<h2>Use of cookies</h2>
<p>Cookies are small pieces of data stored by the Web browser. Cookies are often used to remember information about preferences and pages you have
    visited. For example, when you visit some sites on the Web you might see a &quot;Welcome Back&quot; message. The first time you visited the site a cookie was
    probably set on your computer; when you return, the cookie is read again. Cookies do not contain personally identifiable information, such as names and
    passwords.</p>
<p>The CCLE web site requires that cookies be enabled in your web browser to operate properly. Cookies are used for authentication and to maintain user
    sessions.</p>

<h2>Automatic collection of non-personal data for web site analysis</h2>
<p>The CCLE site collects non-personal information to improve functionality and content and to monitor performance, including the Internet protocol (IP)
    address used to connect your computer to the Internet; your browser type and version, operating system, and platform; and the full Uniform Resource
    Locators (URLs) used in navigation. This information is used only to help us measure the effectiveness of the CCLE site and about the usage and
    performance of the site or its individual web pages.</p>