    /**
     * Holds sections that should have js to collapse/expand.
     *
     * @var array
     */
    private $collapsenodesforjs = array();

    /**
     * Course node to add elements at the end.
     *
     * @var navigation_node
     */
    private $_coursenode = null;

    /**
     * Stores course format object.
     *
     * @var format_base 
     */
    private $format = null;

    /**
     * Moodle's navigation tree.
     *
     * @var navigation_node
