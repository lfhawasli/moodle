    /**
     * Changes or removes icons for certain nodes.
     */
    private function change_icons() {
        global $PAGE;

        $nodestochange['participants']['type'] = \navigation_node::TYPE_CONTAINER;
        $nodestochange['participants']['icon'] = new \pix_icon('i/users', get_string('participants'));
        foreach ($nodestochange as $name => $nodeinfo) {
            if ($node = $this->navigation->find($name, $nodeinfo['type'])) {
                $node->icon = $nodeinfo['icon'];
            }
        }

        // The settingsnav is not set if we are purging caches.
        @$settingsnav = $PAGE->settingsnav; // Supress NOTICE when purging caches.
        if (empty($settingsnav)) {
            return;
        }

        // Add gear icon to Site administration. Treat separately since it is
        // harder to find.
        // Code from lib/navigationlib.php:flat_navigation->initialise().
        $admin = $settingsnav->find('siteadministration', \navigation_node::TYPE_SITE_ADMIN);
        if (!$admin) {
            // Try again - crazy nav tree!
            $admin = $settingsnav->find('root', \navigation_node::TYPE_SITE_ADMIN);
        }
        if ($admin) {
            $admin->icon = new \pix_icon('t/preferences', get_string('administrationsite'));
        }
    }

    /**
     * Rearrange nodes.
     */
    private function rearrange_nodes() {
        global $PAGE;
        
        $nodestomove = array('grades' => \navigation_node::TYPE_SETTING,
            'coursedownload' => \navigation_node::TYPE_SECTION,
            'participants' => \navigation_node::TYPE_CONTAINER);
        foreach ($nodestomove as $name => $type) {
            if ($node = $this->navigation->find($name, $type)) {
                $node->remove();
                
                // If Admin panel is not displayed, then need to set Grades to
                // have a divider.
                if ($name === 'grades' &&
                        !has_capability('format/ucla:viewadminpanel', $PAGE->context)) {
                    $node = new \flat_navigation_node($node, 0);
                    $node->set_showdivider(true);             
                }
                
                $this->coursenode->add_node($node);
            }
        }
    }

    /**
     * Remove items we do not want.
     */
    private function remove_nodes() {
        $nodestoremove = array('badgesview' => \navigation_node::TYPE_SETTING,
            'competencies' => \navigation_node::TYPE_SETTING,
            'home' => \navigation_node::TYPE_ROOTNODE,
            'privatefiles' => \navigation_node::TYPE_SETTING,
            'calendar' => \navigation_node::TYPE_CUSTOM);

        foreach ($nodestoremove as $name => $type) {
            if ($node = $this->navigation->find($name, $type)) {
                $node->remove();
            }
        }

        // Next, we will need the mycourses node and the mycourses node children in any case and don't want to fetch them more
        // than once.
        $mycoursesnode = $this->navigation->find('mycourses', \navigation_node::TYPE_ROOTNODE);
        $mycourseschildrennodeskeys = $mycoursesnode->get_children_key_list();

        if ($mycoursesnode) {
            // Hide mycourses node.
            $mycoursesnode->showinflatnavigation = false;

            // Hide all courses below the mycourses node.
            foreach ($mycourseschildrennodeskeys as $k) {
                $mycoursesnode->get($k)->showinflatnavigation = false;
            }
        }
    }

    /**
     * Change link location for the grades node if set in course settings.
     */
    private function redirectgradelink() {
        global $PAGE;

        $formatoptions = $this->format->get_format_options();
        if (empty($formatoptions['myuclagradelinkredirect'])) {
            return;
        }
        $gradelinknode = $this->navigation->find('grades', \navigation_node::TYPE_SETTING);
        if ($gradelinknode && $formatoptions['myuclagradelinkredirect']) {
            $gradelinknode->action = new \moodle_url('https://be.my.ucla.edu');
        }
    }

    /**
     * Show the roles that a user can switch to.
     */
    private function add_changerole() {
        global $PAGE;

        $courseid = $PAGE->course->id;
        $returnurl = $PAGE->url->out_as_local_url(false);
        $sessionkey = sesskey();

        // If role is already switched, display "Return to normal role".
        if (is_role_switched($courseid)) {
            $rolereturnurl = new \moodle_url('/course/switchrole.php', array(
                'id' => $courseid,
                'sesskey' => $sessionkey,
                'switchrole' => 0,
                'returnurl' => $returnurl
            ));

            $rolereturn = \navigation_node::create(get_string('switchrolereturn'),
                        $rolereturnurl, \navigation_node::TYPE_SETTING,
                        null, 'themeuclasharedswitchrole', new \pix_icon('a/logout', 'normalrole'));
            $this->coursenode->add_node($rolereturn);
        } else {
            // Otherwise display blocks to switch to available roles.
            $pagecontext = \context_course::instance($courseid);
            $roles = get_switchable_roles($pagecontext);
            // Check if there are any roles user can switch to.
            if (is_array($roles) && (count($roles) > 0)) {
                // Create main dropdown node "Switch role to".
                $switchrolenode = \navigation_node::create(get_string('switchroleto'),
                        new \moodle_url('/course/view.php', array('id' => $courseid)), // We have to add a URL to the course node,
                                                                                // otherwise the node wouldn't be added to
                                                                                // the flat navigation by Boost.
                                                                                // There is no better choice than the course
                                                                                // home page.
                        \navigation_node::TYPE_CUSTOM,
                        null,
                        'themeuclasharedswitchrole',
                        new \pix_icon('i/switchrole', 'switchrole'));
                // Prevent that the switch role node is marked as active and added to the breadcrumb when showing the
                // course home page.
                $switchrolenode->make_inactive();
                $switchrolenode->isexpandable = true;

                // Get the user preference for the collapse state of the switch role node and set the collapse and hidden
                // node attributes of the more section node accordingly. At the same time, reallocate the parent of the
                // existing section nodes.
                $userprefswitchrolenode = get_user_preferences('theme_uclashared-collapse_'.
                        'themeuclasharedswitchrole', 1);
                if ($userprefswitchrolenode == 1) {
                    $switchrolenode->collapse = true;
                } else {
                    $switchrolenode->collapse = false;
                }

                // Add the switch role node to the coursehome node.
                $this->coursenode->add_node($switchrolenode);

                // For each role, create new node and add it to "Switch role" node.
                foreach ($roles as $key => $role) {
                    $roleurl = new \moodle_url('/course/switchrole.php', array(
                        'id' => $courseid,
                        'switchrole' => $key,
                        'sesskey' => $sessionkey,
                        'returnurl' => $returnurl
                    ));
                    $rolenode = \navigation_node::create($role,
                            $roleurl,
                            \navigation_node::TYPE_ACTIVITY,
                            null,
                            $key,
                            new \pix_icon('spacer', ''));

                    if ($userprefswitchrolenode == 1) {
                        $rolenode->hidden = true;
                    } else {
                        $rolenode->hidden = false;
                    }

                    // Add the role node to the coursehome node.
                    $this->coursenode->add_node($rolenode);

                    // Need to set parent only after adding it.
                    $rolenode->set_parent($switchrolenode);
                  }

                // Make switch role node collapsible.
                $this->collapsenodesforjs[] = 'themeuclasharedswitchrole';
            }
        }
    }

    /**
     * Apply custom CSS class selectors to course sections if TA site.
     */
    private function tasite_theme() {
        // Check if TA site, indirectly by checking for TA parent site backlink.
        $tasitenode = $this->coursenode->find('parentsitehome' ,\navigation_node::TYPE_SECTION);
        if ($tasitenode) {
            $children = $this->coursenode->get_children_key_list();
            // For each node above and including the showall node, set the tasite parameter.
            foreach ($children as $child) {
                $this->coursenode->find($child ,\navigation_node::TYPE_SECTION)->tasite = 'tasite';
                if ($child == 'showall') {
                    break;
                }
            }
        }
    }

    /**
     * Add Download Course Material link if student.
     */
    private function add_download_course_material() {
        global $CFG, $COURSE;

        // Check if this is a student that can download the archive.
        $coursecontext = \context_course::instance($COURSE->id);
        $isinstructor = has_capability('moodle/course:manageactivities', $coursecontext);
        $canrequest = has_capability('block/ucla_course_download:requestzip', $coursecontext);
        require_once($CFG->dirroot . '/blocks/ucla_course_download/locallib.php');

        if ($isinstructor || !$canrequest || !student_zip_requestable($COURSE)) {
            return;
        }

        // Create the node and add to navigation drawer.
        $coursedownloadnode = \navigation_node::create(get_string('coursedownload', 'block_ucla_course_download'),
                    new \moodle_url('/blocks/ucla_course_download/view.php', array('courseid' => $COURSE->id)),
                    \navigation_node::TYPE_SECTION,
                    null, 'coursedownload', new \pix_icon('t/download', ''));

        $this->coursenode->add_node($coursedownloadnode);
    }
    
    /**
     * Adds text for empty sections and text and color for hidden sections.
     */
    private function add_empty_hidden_text() {
        global $COURSE, $PAGE;
        $sections = get_fast_modinfo($COURSE)->get_section_info_all();
        $emptysectionIDs = array();
        $hiddensectionIDs = array();
        $viewcapability = has_capability('moodle/course:viewhiddensections', $PAGE->context);
        
        foreach ($sections as $section) {
            if (empty($section->sequence) && empty($section->summary)) {
                $emptysectionIDs[$section->id] = null;
            }
            if (!$section->visible) {
                $hiddensectionIDs[$section->id] = null;
            }
        }
        
        $emptysectiontext = get_string('emptysectiontext', 'theme_uclashared');
        $hiddensectiontext = get_string('hiddensectiontext', 'theme_uclashared');
        
        $nodes = $this->navigation->find_all_of_type(\navigation_node::TYPE_SECTION);
        foreach ($nodes as $node) {
            if (array_key_exists($node->key, $emptysectionIDs)) {
                if ($viewcapability) {
                    $node->text .= $emptysectiontext;
                } else {
                    $node->remove();
                    continue;
                }
            }
            if (array_key_exists($node->key, $hiddensectionIDs)) {
                $node->text .= $hiddensectiontext;
                $node->add_class('hidden-node');
            }
        }
    }

    /**
     * Modifies Moodle navigation tree.
     *
     * Called from local/ucla/lib.php: local_ucla_extend_navigation().
     *
     * @param \navigation_node $navigation
     */
    public function run(\navigation_node $navigation) {
        global $PAGE;

        $this->navigation = $navigation;

        // Only make changes for site level or courses with UCLA format.
        if ($PAGE->course->id != SITEID) {
            $this->format = course_get_format($PAGE->course->id);
            if ($this->format->get_format() !== 'ucla') {
                return;
            }
        }

        // Remove nodes.
        $this->remove_nodes();

        // Run course specific changes.
        if ($PAGE->course->id != SITEID) {
            // Add nodes if on course.
            $this->add_syllabus();
            $this->add_tasiteparent();
            $this->add_show_all();
            $this->add_new_section();
            $this->add_libraryreserves();
            $this->add_mediaresources();
            $this->add_activityresources();
            $this->add_more();
            $this->add_editingmode();
            $this->add_adminpanel();
            $this->add_download_course_material();
            $this->add_changerole();
            $this->add_enrolme();

            // Rearrange items.
            $this->rearrange_nodes();

            // Color the nodes if TA site.
            $this->tasite_theme();

            // Redirect grade link to MyUCLA if configured.
            $this->redirectgradelink();

            // Highlight nodes.
            $this->higlight_nodes();
            
            // Modifies display for empty or hidden sections.
            $this->add_empty_hidden_text();
        }

        // Change icons for nodes.
        $this->change_icons();

        // If at least one section needs to be collapsed.
        if (!empty($this->collapsenodesforjs)) {
            // Add JavaScript for collapsing nodes to the page.
            $PAGE->requires->js_call_amd('theme_uclashared/collapsenavdrawernodes', 'init', [$this->collapsenodesforjs]);
            // Allow updating the necessary user preferences via Ajax.
            foreach ($this->collapsenodesforjs as $node) {
                user_preference_allow_ajax_update('theme_uclashared-collapse_'.$node.'node', PARAM_BOOL);
            }
        }
    }
